#include <stdio.h>

int main() {
    
    int A , B ;
    
    
    printf("Enter A : ");
    scanf("%i" , &A );
    
    printf("Enter B : ");
    scanf("%i" , &B );
      
    printf("The Sum is = %i\n" , A+B );
    printf("The Subtraction is = %i\n" , A-B );
    printf("The Multiplication is = %i\n" , A*B );
    printf("The Quotient is = %i\n" , A/B );
    printf("The Remainder is = %i\n" , A%B );
    
    
    
    
    
    
    return 0;
}
